package com.example.coolweather.gson;

public class Cw {

    private String type;
    private String brf;
    private String txt;
    public void setType(String type) {
        this.type = type;
    }
    public String getType() {
        return type;
    }

    public void setBrf(String brf) {
        this.brf = brf;
    }
    public String getBrf() {
        return brf;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }
    public String getTxt() {
        return txt;
    }

}
